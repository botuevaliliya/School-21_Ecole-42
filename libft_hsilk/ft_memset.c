#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	void	*d;
	size_t	n;

	d = b;
	n = 0;
	while (n < len)
	{
		*(unsigned char *)d = c;
		d++;
		n++;
	}
	return (b);
}
